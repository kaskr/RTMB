## WriteLines **only if changed**
writeLines <- function(text, con) {
  if (file.exists(con)) {
    if(identical(readLines(con), text)) {
      cat("No change:", con, "\n")
      return (invisible(NULL))
    }
  }
  cat("Updating: ", con, "\n")
  base::writeLines(text, con)
}

